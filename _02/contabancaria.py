class ContaBancaria:
    def __init__(self):
        self.__cpf = ' '
        self.__agencia = ' '
        self.__nconta = ' '
        self.__senha = ' '

    def get_cpf(self):
        return self.__cpf

    def set_cpf(self, __cpf):
        return self.__cpf

    def get_agencia(self):
        return self.__agencia

    def set_agencia(self, __agencia):
        return self.__agencia

    def get_nconta(self):
        return self.__nconta

    def set_nconta(self, __nconta):
        return self.__nconta

    def get_senha(self):
        return self.__senha

    def set_senha(self, __senha):
        return self.__senha